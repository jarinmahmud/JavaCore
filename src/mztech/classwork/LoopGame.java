package mztech.classwork;

public class LoopGame {
    public static void main(String[] args) {
        // prints 2 times table
        for (int i = 1; i <= 10; i++) {
            System.out.println("2 x " + i + " = " + (2 * i));
        }

        //Print 10 to 1 in reverse order
        for (int j = 10; j > 0; j--) {
            System.out.println(j);
        }
    }
}
