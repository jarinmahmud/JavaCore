public class Multiply {
    public static void multiplier(int numOne, int numTwo) {

        System.out.println(numOne + " x " + numTwo + " = " + numOne * numTwo);

    }

    public static void main(String[] args) {
        multiplier(4, 1);
        multiplier(4, 2);
        multiplier(4, 3);
        multiplier(4, 4);
        multiplier(4, 5);
        multiplier(4, 6);
        multiplier(4, 7);
        multiplier(4, 8);
        multiplier(4, 9);
        multiplier(4, 10);

    }
}
